<p>information</p>
<ul>
    <li><span class="text-bold">le taux horaire brut</span> correspond au salaire avant déduction des charges salariales.</li>
    <li><span class="text-bold">le taux horaire net</span> correspond au taux horaire brut, auquel on déduit les cotisations à la charge du salarié.</li>
    <li><span class="text-bold">le taux horaire porté sur le volet social,</span> correspond au taux horaire net qui peut être majoré de 10% pour 
    tenir compte des congés payés selon certaines conditions. Dans ce cas, pendant ses congés payés, 
    le salarié ne perçoit pas de rémunération.
    </li>
    <li><span class="text-bold">le coût total horaire,</span> correspond au taux brut 
        (comprenant éventuellement la majoration de 10% au titre des congés payés) plus les charges patronales.</li>
    <li><span class="text-bold">le coût horaire total réel,</span> correspond au coût horaire total, 
        auquel on retire d'éventuelles déductions (allègement ou exonération de cotisations patronales,
        réductions d'impôts).
    </li>
</ul>